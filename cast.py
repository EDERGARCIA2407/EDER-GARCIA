#cambio de tipo de variable
#variable a texto
numero = "147"
print(numero)
print(type(numero))

#variable a entero
numero = int(numero)
print(numero)
print(type(numero))

#variable a decimal
numero = float(numero)
print(numero)
print(type(numero))

#variable a tipo texto
numero = complex(numero)
print(numero)
print(type(numero))

